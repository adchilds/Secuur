        char[][] autokeyMappingCopy = TABULA_RECTA;
        SecureRandom random = new SecureRandom();
        int startShift = random.nextInt(26);

        for (int row = 0; row < TABULA_RECTA.length; row++) {
            // Apply the shift to each row
            char[] shiftedChars = SecuurArrayUtils.shift(TABULA_RECTA[row], startShift);
            autokeyMappingCopy[row] = shiftedChars;
        }

        return autokeyMappingCopy;
